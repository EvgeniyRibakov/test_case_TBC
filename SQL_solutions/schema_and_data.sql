
-- Creating the CLIENTS table
CREATE TABLE CLIENTS (
  ClientID INT PRIMARY KEY,
  FirstName VARCHAR(50),
  LastName VARCHAR(50),
  Email VARCHAR(100)
);

-- Creating the ORDERS table with the added Description column
CREATE TABLE ORDERS (
  OrderID INT PRIMARY KEY,
  ClientID INT,
  OrderDate DATE,
  Amount DECIMAL(10, 2),
  Description VARCHAR(100)
);

-- Creating the ADDRESSES table
CREATE TABLE ADDRESSES (
  AddressID INT PRIMARY KEY,
  ClientID INT,
  Street VARCHAR(100),
  City VARCHAR(50),
  State VARCHAR(50),
  PostalCode VARCHAR(10)
);

